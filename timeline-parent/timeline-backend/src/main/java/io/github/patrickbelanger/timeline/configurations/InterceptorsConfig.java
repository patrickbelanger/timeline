package io.github.patrickbelanger.timeline.configurations;

import io.github.patrickbelanger.timeline.interceptors.JWTTokenInterceptor;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class InterceptorsConfig implements WebMvcConfigurer {

    private JWTTokenInterceptor jwtTokenInterceptor;

    public InterceptorsConfig(JWTTokenInterceptor jwtTokenInterceptor) {
        this.jwtTokenInterceptor = jwtTokenInterceptor;
    }

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(jwtTokenInterceptor)
                .excludePathPatterns("/api/v1/authenticate/register", "/api/v1/authenticate/login");
    }

}
